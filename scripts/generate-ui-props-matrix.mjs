#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";

const repoRoot = process.cwd();
const componentsDir = path.join(repoRoot, "packages/ui/components");
const outputPath = path.join(repoRoot, "packages/ui/COMPONENT_PROPS_MATRIX.md");

const skipFiles = new Set(["index.ts", "cn.ts", "chart.ts"]);

function toComponentName(fileName) {
  return fileName.replace(/\.(tsx|ts)$/, "").split("-").map((part) => part[0].toUpperCase() + part.slice(1)).join("");
}

function parseProps(content, fallbackName) {
  const interfaceMatch = content.match(/export interface (\w+Props)[^{]*\{([\s\S]*?)\n\}/m);
  if (!interfaceMatch) {
    return { propsName: `${fallbackName}Props`, fields: [] };
  }

  const propsName = interfaceMatch[1];
  const block = interfaceMatch[2];
  const fields = block
    .split("\n")
    .map((line) => line.trim())
    .filter((line) => line && !line.startsWith("//"))
    .map((line) => line.replace(/;$/, ""))
    .filter((line) => /^[a-zA-Z0-9_]+\??:/.test(line))
    .map((line) => line.split(":")[0]);

  return { propsName, fields };
}

function parseDefaults(content) {
  const defaults = [];
  const forwardRefIndex = content.indexOf("React.forwardRef");
  if (forwardRefIndex < 0) {
    return defaults;
  }

  const afterForwardRef = content.slice(forwardRefIndex);
  const destructureMatch = afterForwardRef.match(/\(\s*\{([\s\S]*?)\}\s*,\s*ref\s*\)\s*=>/m);
  if (!destructureMatch) {
    return defaults;
  }

  const paramsBlock = destructureMatch[1];
  for (const raw of paramsBlock.split(",")) {
    const token = raw.trim();
    if (!token.includes("=")) {
      continue;
    }
    const [name, value] = token.split("=").map((part) => part.trim());
    if (!name || !value) {
      continue;
    }
    defaults.push(`${name}=${value}`);
  }
  return defaults;
}

const files = fs
  .readdirSync(componentsDir)
  .filter((name) => !skipFiles.has(name))
  .filter((name) => name.endsWith(".tsx") || name.endsWith(".ts"))
  .sort();

const rows = [];
for (const fileName of files) {
  const fullPath = path.join(componentsDir, fileName);
  const content = fs.readFileSync(fullPath, "utf8");
  const componentName = toComponentName(fileName);
  const { propsName, fields } = parseProps(content, componentName);
  const defaults = parseDefaults(content);
  rows.push({
    fileName,
    componentName,
    propsName,
    fields,
    defaults
  });
}

const lines = [
  "# UI Component Props Matrix",
  "",
  "Generated by `scripts/generate-ui-props-matrix.mjs`.",
  "",
  `Generated at: ${new Date().toISOString()}`,
  "",
  "| Component | File | Props Type | Key Props | Defaults |",
  "| --- | --- | --- | --- | --- |"
];

for (const row of rows) {
  const keyProps = row.fields.length ? `\`${row.fields.join("`, `")}\`` : "-";
  const defaults = row.defaults.length ? `\`${row.defaults.join("`, `")}\`` : "-";
  lines.push(
    `| ${row.componentName} | \`packages/ui/components/${row.fileName}\` | \`${row.propsName}\` | ${keyProps} | ${defaults} |`
  );
}

lines.push("");

fs.writeFileSync(outputPath, `${lines.join("\n")}\n`, "utf8");
console.log(`generated: ${outputPath}`);
